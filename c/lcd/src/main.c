#define RS 29
#define EN 28
#define D4 21
#define D5 22
#define D6 23
#define D7 24



void initPins();
const uint8_t wifi3[320];
const uint8_t wifi2[320];
const uint8_t wifi1[320];
const uint8_t wifi0[320];

int main() {
const uint8_t *wifiArray[4] = { wifi0, wifi1, wifi2, wifi3 };

	wiringPiSetup();
	initPins();

	lcdCreate(RS, EN, D4, D5, D6, D7);

	lcdClear();
	lcdPrintf("Waiting for");
	lcdSetCursor(0, 1);
	lcdPrintf("Wifi");


	int p; // value showing ping return value
	int i = 0;
	int diff=1;
	do {
		if (i >= 3) {
			i = 3;
			diff = -1;
		}
		else if (i <= 0) {
			i = 0;
			diff = 1;
		}
		drawBitMap(wifiArray[i], 20, 16, 12,0, 0);
		delay (500) ; // mS
		i+=diff;
		++i;
		p = system("ping -c 1 8.8.8.8");
	} while (p != 0 && (delay(500) , 1));

	lcdClear();
	lcdPrintf("Getting");
	lcdSetCursor(0, 1);
	lcdPrintf("IP address");

	int fd;
	struct ifreq ifr;
	fd = socket(AF_INET, SOCK_DGRAM, 0);
	ifr.ifr_addr.sa_family = AF_INET; // IPv4 IP address
	strncpy(ifr.ifr_name, "wlan0", IFNAMSIZ-1);
	ioctl(fd, SIOCGIFADDR, &ifr);
	close(fd);

	char ipAddress[16];
	snprintf(ipAddress, 16, "%s", inet_ntoa(((struct sockaddr_in *)&ifr.ifr_addr)->sin_addr));
	printf("%s\n", ipAddress);
	lcdClear();
	lcdPrintf("%s", ipAddress);
	return 0;
}

